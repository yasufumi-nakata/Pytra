def _load_results(case_root: str) -> dict[str, dict[str, dict[str, object]]]:
    """Return {target: {case_stem: {category, timestamp, detail?}}}."""
    data: dict[str, dict[str, dict[str, object]]] = {}
    for lang in PARITY_LANGS:
        path = PARITY_DIR / f"{lang}_{case_root}.json"
        if not path.exists():
            # Try short name (e.g. powershell → ps1_fixture.json)
            short = _LANG_SHORT.get(lang, lang)
            if short != lang:
                path = PARITY_DIR / f"{short}_{case_root}.json"
        if not path.exists():
            data[lang] = {}
            continue
        try:
            doc = json.loads(path.read_text(encoding="utf-8"))
            results = doc.get("results", {})
            data[lang] = {k: v for k, v in results.items() if isinstance(v, dict)}
        except Exception:
            data[lang] = {}
    return data


def _load_lint_results() -> dict[str, dict[str, int | None] | None]:
    """Return {lang: {"violations": N, "pass_cats": N, "total_cats": N} | None}.

    None value means not implemented. Supports both old (int) and new (dict) JSON format.
    """
    path = PARITY_DIR / "emitter_lint.json"
    if not path.exists():
        return {}
    try:
        doc = json.loads(path.read_text(encoding="utf-8"))
        langs = doc.get("langs", {})
        result: dict[str, dict[str, int | None] | None] = {}
        for k, v in langs.items():
            # Normalize short name to canonical name (e.g. ps1 → powershell)
            canonical = _LANG_SHORT_REV.get(k, k)
            if v is None:
                result[canonical] = None
            elif isinstance(v, dict):
                result[canonical] = {
                    "violations": v.get("violations"),
                    "pass_cats": v.get("pass_cats"),
                    "total_cats": v.get("total_cats"),
                }
            else:
                # 旧フォーマット（int）後方互換
                result[canonical] = {"violations": int(v), "pass_cats": None, "total_cats": None}
        return result
    except Exception:
        return {}


def _load_selfhost_results() -> dict[str, dict[str, object]]:
    """Return {selfhost_lang: selfhost_doc}. Includes 'python' from selfhost_python.json."""
    data: dict[str, dict[str, object]] = {}
    # Load python row from selfhost_python.json (generated by run_selfhost_parity.py)
    python_path = PARITY_DIR / "selfhost_python.json"
    if python_path.exists():
        try:
            doc = json.loads(python_path.read_text(encoding="utf-8"))
            data["python"] = doc if isinstance(doc, dict) else {}
        except Exception:
            data["python"] = {}
    else:
        data["python"] = {}
    for lang in SELFHOST_LANGS:
        path = PARITY_DIR / f"selfhost_{lang}.json"
        if not path.exists():
            data[lang] = {}
            continue
        try:
            doc = json.loads(path.read_text(encoding="utf-8"))
            data[lang] = doc if isinstance(doc, dict) else {}
        except Exception:
            data[lang] = {}
    return data


def _load_emitter_host_results() -> dict[str, dict[str, dict[str, object]]]:
    """Return {host_lang: {hosted_emitter: emitter_host_doc}}.

    Current single-emitter JSON:
      {"host_lang": "go", "hosted_emitter": "cpp", "build_status": "ok", ...}

    N×N JSON:
      {"host_lang": "go", "emitters": {"cpp": {...}, "go": {...}}}
    """
    data: dict[str, dict[str, dict[str, object]]] = {}
    for lang in EMITTER_HOST_LANGS:
        path = PARITY_DIR / f"emitter_host_{lang}.json"
        if not path.exists():
            data[lang] = {}
            continue
        try:
            doc = json.loads(path.read_text(encoding="utf-8"))
            data[lang] = _normalize_emitter_host_doc(doc if isinstance(doc, dict) else {})
        except Exception:
            data[lang] = {}
    return data


def _normalize_emitter_host_doc(doc: dict[str, object]) -> dict[str, dict[str, object]]:
    hosted = doc.get("emitters")
    if isinstance(hosted, dict):
        out: dict[str, dict[str, object]] = {}
        for hosted_lang, entry in hosted.items():
            if isinstance(entry, dict):
                out[str(hosted_lang)] = entry
        return out

    # Accept aliases so hand-written result files are less brittle.
    hosted = doc.get("hosted_emitters")
    if isinstance(hosted, dict):
        out = {}
        for hosted_lang, entry in hosted.items():
            if isinstance(entry, dict):
                out[str(hosted_lang)] = entry
        return out

    hosted = doc.get("results")
    if isinstance(hosted, dict):
        out = {}
        for hosted_lang, entry in hosted.items():
            if isinstance(entry, dict):
                out[str(hosted_lang)] = entry
        return out

    hosted_lang = str(doc.get("hosted_emitter", "cpp"))
    if hosted_lang == "":
        hosted_lang = "cpp"
    return {hosted_lang: doc}


def _selfhost_stage_icon(doc: dict[str, object], emit_lang: str) -> str:
    """Return icon for one cell in the selfhost matrix.

    PASS (🟩): build.status == "ok" AND fixture_fail == 0 AND sample_fail == 0  [S4]
    UNTESTED (⬜): no data, emit_targets[lang] status is not_tested/toolchain_missing/empty
    FAIL (🟥): otherwise (build failed, or any parity fail, or build status != ok)
    """
    stages = doc.get("stages", {})
    emit_targets = doc.get("emit_targets", {})
    if not stages and not emit_targets:
        return "⬜"

    et = emit_targets.get(emit_lang, {}) if isinstance(emit_targets, dict) else {}
    et_status = str(et.get("status", "")) if isinstance(et, dict) else ""
    if et_status in ("not_tested", "toolchain_missing", ""):
        return "⬜"

    if not isinstance(stages, dict):
        return "🟥"

    build = stages.get("build", {})
    build_ok = isinstance(build, dict) and str(build.get("status", "")) == "ok"
    if not build_ok:
        return "🟥"

    # S4: use per-emit-target fixture_fail + sample_fail as definitive PASS condition
    # Both fixture AND sample must be present and have 0 failures for PASS.
    if isinstance(et, dict):
        fixture_fail = et.get("fixture_fail")
        sample_fail  = et.get("sample_fail")
        if fixture_fail is not None and sample_fail is not None:
            return "🟩" if (int(fixture_fail) == 0 and int(sample_fail) == 0) else "🟥"
        # Only one of fixture/sample tested — not enough for PASS
        if fixture_fail is not None or sample_fail is not None:
            return "🟥"

    # Fallback: stages.parity.status (legacy docs without per-target counts)
    parity = stages.get("parity", {})
    parity_ok = isinstance(parity, dict) and str(parity.get("status", "")) == "ok"
    return "🟩" if parity_ok else "🟥"


def _status_ok(value: object) -> bool:
    text = str(value).lower()
    return text == "ok" or text == "pass" or text == "passed" or text == "success"


def _emitter_host_build_icon(doc: dict[str, object]) -> str:
    if not doc:
        return "⬜"
    status = doc.get("build_status")
    if status is None:
        build = doc.get("build")
        if isinstance(build, dict):
            status = build.get("status")
    if status is None:
        return "⬜"
    return "🟩" if _status_ok(status) else "🟥"


def _emitter_host_parity_icon(doc: dict[str, object]) -> str:
    if not doc:
        return "⬜"
    status = doc.get("parity_status")
    if status is None:
        parity = doc.get("parity")
        if isinstance(parity, dict):
            status = parity.get("status")
    if status is not None:
        return "🟩" if _status_ok(status) else "🟥"
    fail = doc.get("parity_fixture_fail")
    passed = doc.get("parity_fixture_pass")
    if fail is None:
        return "⬜"
    try:
        return "🟩" if int(fail) == 0 and passed is not None else "🟥"
    except Exception:
        return "🟥"


def _emitter_host_cell_icon(doc: dict[str, object]) -> str:
    build_icon = _emitter_host_build_icon(doc)
    parity_icon = _emitter_host_parity_icon(doc)
    if build_icon == "⬜" and parity_icon == "⬜":
        return "⬜"
    if build_icon == "🟩" and parity_icon == "🟩":
        return "🟩"
    return "🟥"


def _emitter_host_summary_cell(emitter_host_data: dict[str, dict[str, dict[str, object]]], lang: str) -> str:
    hosted_docs = emitter_host_data.get(lang, {})
    if not hosted_docs:
        return "⬜<br>&nbsp;"
    icons = [_emitter_host_cell_icon(hosted_docs.get(hosted_lang, {})) for hosted_lang in EMITTER_HOST_LANGS]
    tested = [ic for ic in icons if ic != "⬜"]
    if not tested:
        return "⬜<br>&nbsp;"
    passed = icons.count("🟩")
    total = len(icons)
    if passed == total:
        icon = "🟩"
    elif passed > 0:
        icon = "🟨"
    else:
        icon = "🟥"
    return f"{icon}<br>{passed}/{total}"


