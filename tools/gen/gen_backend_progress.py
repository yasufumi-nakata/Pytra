#!/usr/bin/env python3
"""Generate backend progress matrices from accumulated parity results.

Reads .parity-results/*.json and fixture/sample lists, then writes:
  docs/ja/progress-preview/backend-progress-fixture.md
  docs/ja/progress-preview/backend-progress-sample.md
  docs/ja/progress-preview/backend-progress-selfhost.md
  docs/ja/progress-preview/backend-progress-emitter-host.md
  docs/en/progress-preview/backend-progress-fixture.md
  docs/en/progress-preview/backend-progress-sample.md
  docs/en/progress-preview/backend-progress-selfhost.md
  docs/en/progress-preview/backend-progress-emitter-host.md

Generated files are placed in progress-preview/ (git-ignored).
To publish, manually copy from progress-preview/ to progress/.

Note: docs/{ja,en}/progress/index.md is the hand-written cushion page
and is NOT generated by this script.

Usage:
    python3 tools/gen/gen_backend_progress.py
"""

from __future__ import annotations

import json
import sys
from datetime import datetime, timezone
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
FIXTURE_ROOT = ROOT / "test" / "fixture" / "source" / "py"
SAMPLE_ROOT = ROOT / "sample" / "py"
STDLIB_ROOT = ROOT / "test" / "stdlib" / "source" / "py"
PARITY_DIR = ROOT / ".parity-results"

PARITY_LANGS = [
    "cpp", "rs", "cs", "powershell", "js", "ts", "dart", "go",
    "java", "scala", "kotlin", "swift", "ruby", "lua", "php", "nim", "julia", "zig",
]
SELFHOST_LANGS = PARITY_LANGS
EMITTER_HOST_LANGS = PARITY_LANGS

# Short display names for table headers (keep columns narrow)
_LANG_SHORT: dict[str, str] = {
    "powershell": "ps1",
}
# Reverse map: short name → canonical PARITY_LANGS name
_LANG_SHORT_REV: dict[str, str] = {v: k for k, v in _LANG_SHORT.items()}


def _col_name(lang: str) -> str:
    return _LANG_SHORT.get(lang, lang)


# ---------------------------------------------------------------------------
# Icons — only PASS / FAIL / untested
# ---------------------------------------------------------------------------

_CATEGORY_ICON: dict[str, str] = {
    "ok": "🟩",
}
# Everything else (run_failed, transpile_failed, toolchain_missing, timeout, etc.) → 🟥

_SELFHOST_STAGE_ICON: dict[str, str] = {
    "not_reached": "⬜",
    "not_tested": "⬜",
    "fail": "🟥",
    "ok": "🟩",
}


def _case_icon(category: str | None) -> str:
    if category is None:
        return "⬜"
    return _CATEGORY_ICON.get(category, "🟥")


# ---------------------------------------------------------------------------
# Case collection
# ---------------------------------------------------------------------------

def _collect_fixture_cases() -> list[tuple[str, str]]:
    """Return (category, stem) pairs sorted by category then stem."""
    out: list[tuple[str, str]] = []
    for cat_dir in sorted(FIXTURE_ROOT.iterdir()):
        if not cat_dir.is_dir():
            continue
        for f in sorted(cat_dir.rglob("*.py")):
            stem = f.stem
            if stem == "__init__" or stem.startswith("ng_"):
                continue
            out.append((cat_dir.name, stem))
    return out


def _collect_sample_cases() -> list[str]:
    out: list[str] = []
    for p in sorted(SAMPLE_ROOT.glob("*.py")):
        stem = p.stem
        if stem == "__init__":
            continue
        out.append(stem)
    return out


def _collect_stdlib_cases() -> list[tuple[str, str]]:
    """Return (module, stem) pairs sorted by module then stem."""
    out: list[tuple[str, str]] = []
    if not STDLIB_ROOT.exists():
        return out
    for mod_dir in sorted(STDLIB_ROOT.iterdir()):
        if not mod_dir.is_dir():
            continue
        for f in sorted(mod_dir.glob("*.py")):
            stem = f.stem
            if stem == "__init__":
                continue
            out.append((mod_dir.name, stem))
    return out


