def _build_parity_matrix(cases: list[tuple[str, str] | str], results: dict[str, dict[str, dict[str, object]]], case_root: str) -> list[str]:
    """Build markdown table rows for fixture or sample parity matrix."""
    lines: list[str] = []
    lang_list = PARITY_LANGS

    if case_root == "fixture":
        lines.append(f"| カテゴリ | ケース | {' | '.join(_col_name(l) for l in lang_list)} |")
        lines.append(f"|---|---|{'|'.join(['---:'] * len(lang_list))}|")
        for item in cases:
            cat, stem = item  # type: ignore[misc]
            cells = []
            for lang in lang_list:
                entry = results.get(lang, {}).get(stem)
                if entry is None:
                    cells.append("⬜")
                else:
                    cells.append(_case_icon(str(entry.get("category", ""))))
            lines.append(f"| {cat} | {stem} | {' | '.join(cells)} |")
    else:
        lines.append(f"| ケース | {' | '.join(_col_name(l) for l in lang_list)} |")
        lines.append(f"|---|{'|'.join(['---:'] * len(lang_list))}|")
        for item in cases:
            stem = item  # type: ignore[assignment]
            cells = []
            for lang in lang_list:
                entry = results.get(lang, {}).get(stem)  # type: ignore[arg-type]
                if entry is None:
                    cells.append("⬜")
                else:
                    cells.append(_case_icon(str(entry.get("category", ""))))
            lines.append(f"| {stem} | {' | '.join(cells)} |")

    # Summary rows — count only cases in the fixture/sample list
    total_cases = len(cases)
    case_stems: set[str] = set()
    for item in cases:
        if case_root == "fixture":
            case_stems.add(item[1])  # type: ignore[index]
        else:
            case_stems.add(item)  # type: ignore[arg-type]

    ok_cells = []
    fail_cells = []
    untested_cells = []
    stale_keys: dict[str, list[str]] = {}
    for lang in lang_list:
        ok_count = 0
        fail_count = 0
        lang_results = results.get(lang, {})
        for stem in case_stems:
            entry = lang_results.get(stem)
            if entry is None:
                continue
            cat = str(entry.get("category", "")) if isinstance(entry, dict) else ""
            if _case_icon(cat) == "🟩":
                ok_count += 1
            else:
                fail_count += 1
        # Detect stale keys (in JSON but not in fixture list)
        extra = set(lang_results.keys()) - case_stems
        if len(extra) > 0:
            stale_keys[lang] = sorted(extra)
        untested_count = total_cases - ok_count - fail_count
        if ok_count + fail_count == 0:
            ok_cells.append("—")
            fail_cells.append("—")
            untested_cells.append(str(total_cases))
        else:
            ok_cells.append(str(ok_count) if ok_count > 0 else "—")
            fail_cells.append(str(fail_count) if fail_count > 0 else "—")
            untested_cells.append(str(untested_count) if untested_count > 0 else "—")

    if case_root == "fixture":
        lines.append(f"| | **🟩 PASS** | {' | '.join(ok_cells)} |")
        lines.append(f"| | **🟥 FAIL** | {' | '.join(fail_cells)} |")
        lines.append(f"| | **⬜ 未実行** | {' | '.join(untested_cells)} |")
    else:
        lines.append(f"| **🟩 PASS** | {' | '.join(ok_cells)} |")
        lines.append(f"| **🟥 FAIL** | {' | '.join(fail_cells)} |")
        lines.append(f"| **⬜ 未実行** | {' | '.join(untested_cells)} |")

    return lines


