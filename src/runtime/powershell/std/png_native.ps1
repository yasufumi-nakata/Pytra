# PNG write stub for PowerShell target

function write_rgb_png {
    param([string]$path, [int]$w, [int]$h, [object]$pixels_)
    $abs_path = [System.IO.Path]::GetFullPath($path)
    $dir = [System.IO.Path]::GetDirectoryName($abs_path)
    if ($dir -and -not (Test-Path $dir)) { [void][System.IO.Directory]::CreateDirectory($dir) }
    # Write a minimal placeholder PNG (1x1 white pixel PNG - fixed bytes)
    $png_bytes = [byte[]]@(
        0x89,0x50,0x4E,0x47,0x0D,0x0A,0x1A,0x0A,  # signature
        0x00,0x00,0x00,0x0D,0x49,0x48,0x44,0x52,  # IHDR length+type
        0x00,0x00,0x00,0x01,0x00,0x00,0x00,0x01,  # width=1 height=1
        0x08,0x02,0x00,0x00,0x00,0x90,0x77,0x53,  # bit_depth=8 color=rgb + crc
        0xDE,0x00,0x00,0x00,0x0C,0x49,0x44,0x41,  # crc + IDAT length+type
        0x54,0x08,0xD7,0x63,0xF8,0xCF,0xC0,0x00,  # IDAT type + zlib data
        0x00,0x00,0x02,0x00,0x01,0xE2,0x21,0xBC,  # zlib data + crc
        0x33,0x00,0x00,0x00,0x00,0x49,0x45,0x4E,  # crc + IEND length+type
        0x44,0xAE,0x42,0x60,0x82                  # IEND type + crc
    )
    [System.IO.File]::WriteAllBytes($abs_path, $png_bytes)
}
