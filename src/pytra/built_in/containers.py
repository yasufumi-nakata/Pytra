    def join(self, iterable: list[str]) -> str: ...

    def strip(self) -> str: ...

    def lstrip(self) -> str: ...

    def rstrip(self) -> str: ...

    def replace(self, old: str, new: str) -> str: ...

    def startswith(self, prefix: str) -> bool: ...

